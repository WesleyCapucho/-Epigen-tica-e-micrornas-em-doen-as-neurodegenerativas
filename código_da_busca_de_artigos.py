# ============================================
# 2) Scoring de relevância (ranqueamento)
#    - Pesos maiores no Título, médios em Abstract e pequenos em Keywords
# ============================================
keywords = [
    "mirna", "microrna", "micro-rna",
    "epigenet", "methylation", "histone",
    "alzheimer", "parkinson",
    "biomarker", "expression", "profiling",
    "exosome", "extracellular vesicle", "csf", "plasma", "serum",
    "brain", "cortex", "hippocampus", "substantia nigra", "striatum"
]
def score_row(row):
    t = row["Title_lower"]
    a = row["Abstract_lower"]
    k = row["Keywords_lower"]
    s = 0
    for kw in keywords:
        if kw in t: s += 3
        if kw in a: s += 2
        if kw in k: s += 1
    return s

df["relevance_score"] = df.apply(score_row, axis=1)
df_scored = df[df["relevance_score"] > 0].copy()
