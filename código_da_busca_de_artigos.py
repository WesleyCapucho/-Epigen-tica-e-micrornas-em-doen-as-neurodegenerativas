def detect_disease(text):
    t = str(text).lower()
    has_ad = any(x in t for x in ["alzheimer", "alzheimer’s", "alzheimer's"])
    has_pd = any(x in t for x in ["parkinson", "parkinson’s", "parkinson's"])
    if has_ad and has_pd: return "AD+PD"
    if has_ad: return "AD"
    if has_pd: return "PD"
    return "Indefinido"

def detect_study_type(text):
    t = str(text).lower()
    if "systematic review" in t or "meta-analysis" in t or "meta analysis" in t:
        return "Revisão/Meta-análise"
    if "review" in t:
        return "Revisão"
    clinical = ["case-control","cohort","cross-sectional","clinical","trial","patients","controls"]
    if any(x in t for x in clinical): return "Primário (clínico)"
    preclin = ["in vitro","cell","cells","neuron","neuronal","mouse","mice","rat","animal","drosophila","zebrafish"]
    if any(x in t for x in preclin): return "Primário (pré-clínico)"
    return "Indefinido"

def detect_method(text):
    t = str(text).lower()
    if "rna-seq" in t or "rna seq" in t or "small rna" in t or "next-generation sequencing" in t:
        return "RNA-seq/smallRNA-seq"
    if "qpcr" in t or "rt-qpcr" in t or "real-time pcr" in t or "rt-pcr" in t or "pcr" in t:
        return "qPCR/RT-qPCR"
    if "microarray" in t: return "Microarray"
    if "nanostring" in t: return "NanoString"
    if "bioinformatic" in t or "in silico" in t or "computational" in t:
        return "Bioinformática"
    return "Indefinido"

def detect_sample(text):
    t = str(text).lower()
    sample_map = [
        ("CSF", ["csf","cerebrospinal fluid"]),
        ("Plasma", ["plasma"]),
        ("Soro", ["serum"]),
        ("Sangue total", ["whole blood","peripheral blood","blood sample","blood plasma","blood serum"]),
        ("Cérebro/tecido", ["brain","hippocampus","cortex","prefrontal","substantia nigra","striatum","temporal","frontal"]),
        ("Exossomos/EVs", ["exosome","exosomes","extracellular vesicle","extracellular vesicles","evs"]),
    ]
    found = []
    for label, keys in sample_map:
        if any(k in t for k in keys):
            found.append(label)
    return "; ".join(sorted(set(found))) if found else "Indefinido"

mirna_pattern = re.compile(r'\bmiR[- ]?\d+[a-z]?(?:-\d+)?[a-z]?(?:-\d+)?\b', re.IGNORECASE)
def extract_mirnas(text):
    s = str(text)
    hits = mirna_pattern.findall(s)
    norm = []
    for m in hits:
        m = m.replace(" ", "-")
        m = "miR-" + m.split("miR-")[-1]
        norm.append(m)
    # únicos preservando ordem
    seen, uniq = set(), []
    for x in norm:
        if x not in seen:
            seen.add(x)
            uniq.append(x)
    return ", ".join(uniq[:12])

