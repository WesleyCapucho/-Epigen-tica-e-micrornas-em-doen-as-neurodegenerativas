# --- PARÂMETROS DA BUSCA ---
query_core = ('(microRNA[Title/Abstract] OR miRNA[Title/Abstract] OR "micro-RNA"[Title/Abstract]) '
              'AND (Alzheimer[Title/Abstract] OR Parkinson[Title/Abstract] OR neurodegenerat*[Title/Abstract]) '
              'AND (expression[Title/Abstract] OR biomarker[Title/Abstract] OR epigenetic[Title/Abstract] '
              'OR methylation[Title/Abstract] OR "expression profiling"[Title/Abstract] OR "RNA-seq"[Title/Abstract] '
              'OR microarray[Title/Abstract])')

# filtro de data (últimos 10 anos: 2015-01-01 até 2025-10-21)
date_from = "2015/01/01"
date_to = "2025/10/21"   # data de corte conforme seu fuso/escopo
date_filter = f' AND ("{date_from}"[PDAT] : "{date_to}"[PDAT])'

query = query_core + date_filter
print("Query usada:\n", query)

# quantidade máxima a recuperar nesta execução (ajuste conforme necessidade)
MAX_RESULTS = 1000

# --- 1) Buscar PMIDs (esearch) ---
search_handle = Entrez.esearch(db="pubmed", term=query, retmax=MAX_RESULTS, usehistory="y")
search_results = Entrez.read(search_handle)
search_handle.close()

total_found = int(search_results["Count"])
pmids = search_results["IdList"]
print(f"Total de registros no PubMed (query + período): {total_found}")
print(f"Baixando os primeiros {len(pmids)} PMIDs (retmax={MAX_RESULTS})")

# --- 2) Recuperar detalhes (efetch Medline) em lotes ---
records = []
BATCH = 200
for i in tqdm(range(0, len(pmids), BATCH)):
    batch = pmids[i:i+BATCH]
    handle = Entrez.efetch(db="pubmed", id=",".join(batch), rettype="medline", retmode="text")
    parsed = Medline.parse(handle)
    for rec in parsed:
        pmid = rec.get("PMID","")
        title = rec.get("TI","")
        abstract = rec.get("AB","")
        journal = rec.get("JT","")
        year = rec.get("DP","").split()[0] if rec.get("DP") else ""
        authors = "; ".join(rec.get("AU", []))
        mesh = "; ".join(rec.get("MH", []))
        records.append({
            "PMID": pmid,
            "Title": title,
            "Abstract": abstract,
            "Journal": journal,
            "Year": year,
            "Authors": authors,
            "MeSH": mesh
        })
    handle.close()
    time.sleep(0.4)  # cortesia ao NCBI

# --- 3) Salvar CSV e RIS (pronto para Rayyan/Zotero) ---
df = pd.DataFrame(records)
CSV_OUT = "pubmed_results_2015_2025.csv"
RIS_OUT = "pubmed_results_2015_2025.ris"
df.to_csv(CSV_OUT, index=False, encoding="utf-8")
print(f"CSV salvo: {CSV_OUT}")

def to_ris(rec):
    ris = ["TY  - JOUR"]
    if rec.get("Authors"):
        for au in rec["Authors"].split("; "):
            ris.append(f"AU  - {au}")
    ris.append(f"TI  - {rec.get('Title','')}")
    if rec.get("Journal"):
        ris.append(f"JO  - {rec.get('Journal')}")
    if rec.get("Year"):
        ris.append(f"PY  - {rec.get('Year')}")
    if rec.get("Abstract"):
        ris.append(f"AB  - {rec.get('Abstract')}")
    ris.append(f"ID  - {rec.get('PMID')}")
    ris.append("ER  - ")
    return "\n".join(ris)

with open(RIS_OUT, "w", encoding="utf-8") as fh:
    for _, r in df.iterrows():
        fh.write(to_ris(r) + "\n")

print(f"RIS salvo: {RIS_OUT}")

