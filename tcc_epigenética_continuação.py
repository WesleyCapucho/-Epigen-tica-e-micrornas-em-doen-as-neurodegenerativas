import numpy as np
from scipy.integrate import solve_ivp
import matplotlib.pyplot as plt

# Variáveis:
# y[0] = BACE1
# y[1] = Aβ
# y[2] = Neuroinflamação
# y[3] = Sobrevivência neuronal

def modelo_AD(t, y, mirna, params):
    BACE1, Abeta, inflam, neuron = y

    alpha_bace1 = params["alpha_bace1"]
    beta_mirna = params["beta_mirna"]
    delta_bace1 = params["delta_bace1"]

    alpha_abeta = params["alpha_abeta"]
    delta_abeta = params["delta_abeta"]

    alpha_inflam = params["alpha_inflam"]
    delta_inflam = params["delta_inflam"]

    beta_neuron = params["beta_neuron"]

    dBACE1_dt = alpha_bace1 - beta_mirna * mirna * BACE1 - delta_bace1 * BACE1
    dAbeta_dt = alpha_abeta * BACE1 - delta_abeta * Abeta
    dInflam_dt = alpha_inflam * Abeta - delta_inflam * inflam
    dNeuron_dt = - beta_neuron * inflam * neuron

    return [dBACE1_dt, dAbeta_dt, dInflam_dt, dNeuron_dt]


params_AD = {
    "alpha_bace1": 1.0,
    "beta_mirna": 0.8,
    "delta_bace1": 0.15,
    "alpha_abeta": 0.7,
    "delta_abeta": 0.12,
    "alpha_inflam": 0.5,
    "delta_inflam": 0.1,
    "beta_neuron": 0.015
}

