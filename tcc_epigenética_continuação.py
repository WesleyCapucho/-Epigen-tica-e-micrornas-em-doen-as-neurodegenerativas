# HEATMAP 2 — miRNAs por tipo de amostra
# ======================================================

sample_col = "Amostra (auto)"

# expandir múltiplas amostras separadas por ;
rows = []

for _, row in top100.iterrows():
    samples = str(row[sample_col]).split(";")
    samples = [s.strip() for s in samples if s.strip() and s.strip() != "Indefinido"]

    for sample in samples:
        for mir in row["miRNAs_list"]:
            rows.append([sample, mir])

df_sample_mirna = pd.DataFrame(rows, columns=["Amostra", "miRNA"])

# manter só principais amostras e miRNAs
top_samples = df_sample_mirna["Amostra"].value_counts().head(8).index.tolist()
df_sample_mirna = df_sample_mirna[
    (df_sample_mirna["Amostra"].isin(top_samples)) &
    (df_sample_mirna["miRNA"].isin(top_mirnas))
]

df_heatmap_sample = pd.crosstab(
    df_sample_mirna["Amostra"],
    df_sample_mirna["miRNA"]
)

df_heatmap_sample = df_heatmap_sample.reindex(columns=top_mirnas, fill_value=0)

plt.figure(figsize=(16, 6))
sns.heatmap(
    df_heatmap_sample,
    annot=True,
    fmt="d",
    cmap="magma",
    linewidths=0.5,
    linecolor="white"
)

plt.title("Distribuição dos principais miRNAs por tipo de amostra")
plt.xlabel("miRNAs")
plt.ylabel("Tipo de amostra")
plt.xticks(rotation=45, ha="right")
plt.tight_layout()

plt.savefig("heatmap_miRNAs_por_amostra.png", dpi=300, bbox_inches="tight")
plt.show()

