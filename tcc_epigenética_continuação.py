# HEATMAP 3 — miRNAs por método experimental
# ======================================================

method_col = "Método (auto)"

rows = []

for _, row in top100.iterrows():
    method = row[method_col]

    if pd.isna(method) or method == "Indefinido":
        continue

    for mir in row["miRNAs_list"]:
        rows.append([method, mir])

df_method_mirna = pd.DataFrame(rows, columns=["Método", "miRNA"])

df_method_mirna = df_method_mirna[
    df_method_mirna["miRNA"].isin(top_mirnas)
]

df_heatmap_method = pd.crosstab(
    df_method_mirna["Método"],
    df_method_mirna["miRNA"]
)

df_heatmap_method = df_heatmap_method.reindex(columns=top_mirnas, fill_value=0)

